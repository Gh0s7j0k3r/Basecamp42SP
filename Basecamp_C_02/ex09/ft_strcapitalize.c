char	*ft_strcapitalize(char *str)
{
	int		i;
	int		w;
	char	c;

	i = 0;
	w = 1;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if (w == 1 && c >= 'a' && c <= 'z')
			str[i] -= 32;
		else if (w == 0 && c >= 'A' && c <= 'Z')
			str[i] += 32;
		if (c < '0' || (c > '9' && c < 'A') || (c > 'Z' && c < 'a') || c > 122)
			w = 1;
		else
			w = 0;
		i++;
	}
	return (str);
}
